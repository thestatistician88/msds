#' Italian Economics Faculties Dataset (2009/2010)
#'
#' A dataset containing performance indicators for 55 Italian Economics faculties
#' during the 2009/2010 academic year, used for evaluating productivity and
#' teaching quality \insertCite{ICR}{msds} .
#'
#' @format A data frame with 55 observations on the following 14 variables:
#' \describe{
#'   \item{\code{University_Type}}{Type of university: Private or Public (Factor)}
#'   \item{\code{P1}}{Rate of persistence between the first and second year (Numeric)}
#'   \item{\code{P2}}{Achieved credits (Numeric)}
#'   \item{\code{P3A}}{Rate of regular students in 3-year bachelor programmes (Numeric)}
#'   \item{\code{P3B}}{Rate of regular students in 2-year master programmes (Numeric)}
#'   \item{\code{P4A}}{Rate of regular graduates in 3-year bachelor programmes (Numeric)}
#'   \item{\code{P4B}}{Rate of regular graduates in 2-year master programmes (Numeric)}
#'   \item{\code{D1}}{Permanent professors per credits (Numeric)}
#'   \item{\code{D2}}{Permanent professors per enrolled student (Numeric)}
#'   \item{\code{D3}}{Seats per enrolled student (2009/2010) (Numeric)}
#'   \item{\code{D4}}{Seats per enrolled student (2008/2009) (Numeric)}
#'   \item{\code{D5}}{Researchers to professors ratio (Numeric)}
#'   \item{\code{D6}}{Monitored teaching activities (Numeric)}
#' }
#'
#' @details
#' The dataset includes 6 productivity indicators (P1-P4B) and 6 teaching
#' indicators (D1-D6) to evaluate the performance of 55 Italian Economics
#' faculties in the 2009/2010 academic year in \insertCite{ICR}{msds}.
#'
#' @source \insertRef{ICR}{msds}
#' \url{https://search.r-project.org/CRAN/refmans/datasetsICR/html/Economics.html}
